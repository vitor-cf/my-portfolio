// HTML

function mudaImagemColorida () {
    
    let noColor = document.getElementById('iconNotColor');
    noColor.style.display = 'none';

    let yesColor = document.getElementById('iconColor');
    yesColor.style.display = 'block';
}  

function returnIcon () {

    let noColor = document.getElementById('iconNotColor');
    noColor.style.display = 'block';

    let yesColor = document.getElementById('iconColor');
    yesColor.style.display = 'none';

}


const iconPass = document.getElementById('iconNotColor');
iconPass.addEventListener('mouseover', mudaImagemColorida); 

const teste = document.getElementById('iconColor');
teste.addEventListener('mouseout', returnIcon);


